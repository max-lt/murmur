/// Scan a local directory recursively, returning a map of relative_path → full_path.
///
/// Returns all regular files; ignore-pattern filtering is the caller's
/// responsibility.
fn scan_local_dir(root: &Path, dir: &Path) -> Result<HashMap<String, PathBuf>> {
    let mut result = HashMap::new();

    if !dir.exists() {
        return Ok(result);
    }

    for entry in std::fs::read_dir(dir).with_context(|| format!("read dir {}", dir.display()))? {
        let entry = entry?;
        let path = entry.path();
        let file_type = entry.file_type()?;

        if file_type.is_dir() {
            let sub = scan_local_dir(root, &path)?;
            result.extend(sub);
        } else if file_type.is_file()
            && let Ok(rel) = path.strip_prefix(root)
        {
            let rel_str = rel.to_string_lossy().replace('\\', "/");
            if !rel_str.is_empty() {
                result.insert(rel_str, path);
            }
        }
    }

    Ok(result)
}

/// Compute blake3 hash of a file (streaming, 64 KiB buffers).
fn hash_file(path: &Path) -> Result<BlobHash> {
    let mut file = std::fs::File::open(path).with_context(|| format!("open {}", path.display()))?;
    let mut hasher = blake3::Hasher::new();
    let mut buf = [0u8; 65536];
    loop {
        let n = file.read(&mut buf)?;
        if n == 0 {
            break;
        }
        hasher.update(&buf[..n]);
    }
    Ok(BlobHash::from_bytes(*hasher.finalize().as_bytes()))
}

/// Get a file's OS modification time as seconds since Unix epoch.
fn file_modified_time(path: &Path) -> u64 {
    std::fs::metadata(path)
        .ok()
        .and_then(|m| m.modified().ok())
        .and_then(|t| t.duration_since(std::time::UNIX_EPOCH).ok())
        .map(|d| d.as_secs())
        .unwrap_or(0)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_scan_local_dir() {
        let dir = tempfile::tempdir().unwrap();
        let root = dir.path();

        std::fs::create_dir_all(root.join("photos")).unwrap();
        std::fs::write(root.join("readme.txt"), "hello").unwrap();
        std::fs::write(root.join("photos/img001.jpg"), "image data").unwrap();
        std::fs::write(root.join("photos/img002.jpg"), "image data 2").unwrap();

        let result = scan_local_dir(root, root).unwrap();
        assert_eq!(result.len(), 3);
        assert!(result.contains_key("readme.txt"));
        assert!(result.contains_key("photos/img001.jpg"));
        assert!(result.contains_key("photos/img002.jpg"));
    }

    #[test]
    fn test_scan_empty_dir() {
        let dir = tempfile::tempdir().unwrap();
        let result = scan_local_dir(dir.path(), dir.path()).unwrap();
        assert!(result.is_empty());
    }

    #[test]
    fn test_scan_nonexistent_dir() {
        let result = scan_local_dir(Path::new("/nonexistent"), Path::new("/nonexistent")).unwrap();
        assert!(result.is_empty());
    }

    #[test]
    fn test_hash_file() {
        let dir = tempfile::tempdir().unwrap();
        let path = dir.path().join("test.bin");
        let data = b"hello world";
        std::fs::write(&path, data).unwrap();

        let hash = hash_file(&path).unwrap();
        let expected = BlobHash::from_data(data);
        assert_eq!(hash, expected);
    }

    #[test]
    fn test_write_file_to_disk() {
        let dir = tempfile::tempdir().unwrap();
        let suppressor = EchoSuppressor::new();
        let folder = SyncedFolder {
            folder_id: FolderId::from_bytes([1u8; 32]),
            local_path: dir.path().to_path_buf(),
            mode: SyncMode::ReadWrite,
        };

        write_file_to_disk(&folder, "subdir/file.txt", b"content", &suppressor).unwrap();

        let full_path = dir.path().join("subdir/file.txt");
        assert!(full_path.exists());
        assert_eq!(std::fs::read_to_string(&full_path).unwrap(), "content");

        // Should be echo-suppressed.
        assert!(suppressor.is_echo(folder.folder_id, "subdir/file.txt"));
    }

    #[test]
    fn test_delete_file_from_disk() {
        let dir = tempfile::tempdir().unwrap();
        let suppressor = EchoSuppressor::new();
        let folder = SyncedFolder {
            folder_id: FolderId::from_bytes([1u8; 32]),
            local_path: dir.path().to_path_buf(),
            mode: SyncMode::ReadWrite,
        };

        let subdir = dir.path().join("subdir");
        std::fs::create_dir_all(&subdir).unwrap();
        std::fs::write(subdir.join("file.txt"), "content").unwrap();

        delete_file_from_disk(&folder, "subdir/file.txt", &suppressor).unwrap();

        assert!(!subdir.join("file.txt").exists());
        // Empty subdir should be cleaned up.
        assert!(!subdir.exists());
    }

    #[test]
    fn test_delete_nonexistent_file() {
        let dir = tempfile::tempdir().unwrap();
        let suppressor = EchoSuppressor::new();
        let folder = SyncedFolder {
            folder_id: FolderId::from_bytes([1u8; 32]),
            local_path: dir.path().to_path_buf(),
            mode: SyncMode::ReadWrite,
        };

        // Should not error.
        delete_file_from_disk(&folder, "nonexistent.txt", &suppressor).unwrap();
    }

    #[test]
    fn test_reverse_sync_skips_existing_correct_file() {
        let dir = tempfile::tempdir().unwrap();
        let _folder = SyncedFolder {
            folder_id: FolderId::from_bytes([1u8; 32]),
            local_path: dir.path().to_path_buf(),
            mode: SyncMode::ReadWrite,
        };

        // Write a file that already has the expected content.
        let data = b"hello world";
        let full_path = dir.path().join("test.txt");
        std::fs::write(&full_path, data).unwrap();

        let blob_hash = BlobHash::from_data(data);
        let _suppressor = EchoSuppressor::new();

        // reverse_sync_file should skip since content matches.
        // We can't easily test with real Storage, but we can test the hash-check path:
        assert!(full_path.is_file());
        let existing_hash = hash_file(&full_path).unwrap();
        assert_eq!(existing_hash, blob_hash);
    }

    #[test]
    fn test_file_modified_time() {
        let dir = tempfile::tempdir().unwrap();
        let path = dir.path().join("test.txt");
        std::fs::write(&path, "hello").unwrap();

        let mtime = file_modified_time(&path);
        assert!(mtime > 0);
    }
}
