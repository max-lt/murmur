/// Perform initial scan of a folder directory, reconciling local files with DAG state.
///
/// - Local file not in DAG → `add_file_streaming()` (read-write only)
/// - DAG file not on disk → write from blob store
/// - Both exist, same hash → skip
/// - Both exist, different hash → `modify_file()` (read-write only)
///
/// Applies the ignore filter so that `.DS_Store`, `.tmp` files, etc. are never
/// added to the DAG.
pub fn initial_scan(
    folder: &SyncedFolder,
    engine: &mut MurmurEngine,
    storage: &Storage,
    echo_suppressor: &EchoSuppressor,
    ignore_filter: &IgnoreFilter,
) -> Result<()> {
    let folder_id = folder.folder_id;

    info!(
        folder_id = %hex::encode(folder_id.as_bytes()),
        path = %folder.local_path.display(),
        "starting initial scan"
    );

    // Get current DAG state for this folder.
    let dag_files: HashMap<String, FileMetadata> = engine
        .folder_files(folder_id)
        .into_iter()
        .map(|m| (m.path.clone(), m))
        .collect();

    // Scan local directory.
    let local_files = scan_local_dir(&folder.local_path, &folder.local_path)?;

    // Local files not in DAG → add them (only if read-write).
    if folder.mode == SyncMode::ReadWrite {
        for (rel_path, full_path) in &local_files {
            // Apply ignore patterns — don't add OS cruft to DAG.
            if ignore_filter.is_ignored(Path::new(rel_path), false) {
                debug!(path = %rel_path, "initial scan: skipping ignored file");
                continue;
            }

            if !dag_files.contains_key(rel_path) {
                info!(path = %rel_path, "initial scan: local file not in DAG, adding");
                match engine.add_file_streaming(folder_id, rel_path, full_path) {
                    Ok(_entry) => {
                        debug!(path = %rel_path, "initial scan: added file to DAG");
                    }
                    Err(e) => {
                        error!(path = %rel_path, error = %e, "initial scan: failed to add file");
                    }
                }
            } else if let Some(dag_meta) = dag_files.get(rel_path) {
                // Both exist — read once, hash once, check for difference.
                let data = std::fs::read(full_path)
                    .with_context(|| format!("read {}", full_path.display()))?;
                let local_hash = BlobHash::from_data(&data);

                if local_hash != dag_meta.blob_hash {
                    info!(path = %rel_path, "initial scan: local file modified since last sync");
                    let size = data.len() as u64;
                    let modified_at = file_modified_time(full_path);
                    let new_meta = FileMetadata {
                        blob_hash: local_hash,
                        folder_id,
                        path: rel_path.clone(),
                        size,
                        mime_type: dag_meta.mime_type.clone(),
                        created_at: dag_meta.created_at,
                        modified_at,
                        device_origin: engine.device_id(),
                    };
                    match engine.modify_file(folder_id, rel_path, new_meta, data) {
                        Ok(_entry) => {
                            debug!(path = %rel_path, "initial scan: updated file in DAG");
                        }
                        Err(e) => {
                            error!(path = %rel_path, error = %e, "initial scan: failed to modify file");
                        }
                    }
                }
            }
        }
    }

    // DAG files not on local disk → write from blob store.
    for (rel_path, dag_meta) in &dag_files {
        if !local_files.contains_key(rel_path) {
            info!(path = %rel_path, "initial scan: DAG file not on disk, writing");
            if let Some(data) = storage.load_blob(dag_meta.blob_hash)? {
                write_file_to_disk(folder, rel_path, &data, echo_suppressor)?;
            } else {
                warn!(
                    path = %rel_path,
                    blob_hash = %dag_meta.blob_hash,
                    "initial scan: blob not available in store"
                );
            }
        }
    }

    info!(
        folder_id = %hex::encode(folder_id.as_bytes()),
        local_count = local_files.len(),
        dag_count = dag_files.len(),
        "initial scan complete"
    );

    Ok(())
}

