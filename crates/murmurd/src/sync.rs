//! Bidirectional sync between the DAG and local filesystem.
//!
//! - **Forward sync**: local filesystem changes (from `FolderWatcher`) → engine operations
//! - **Reverse sync**: engine events (from remote peers) → local filesystem writes
//! - **Initial scan**: reconcile DAG state with local directory on startup

use std::collections::HashMap;
use std::io::Read;
use std::path::{Path, PathBuf};

use anyhow::{Context, Result};
use murmur_engine::{EngineEvent, MurmurEngine};
use murmur_types::{BlobHash, FileMetadata, FolderId, SyncMode};
use tracing::{debug, error, info, warn};

use crate::ignore::IgnoreFilter;
use crate::storage::Storage;
use crate::watcher::{EchoSuppressor, FolderEvent, FsEventKind};

/// Configuration for a synced folder on this device.
#[derive(Debug, Clone)]
pub struct SyncedFolder {
    /// The folder ID.
    pub folder_id: FolderId,
    /// Local directory path.
    pub local_path: PathBuf,
    /// Sync mode (read-write or read-only).
    pub mode: SyncMode,
}

// ---------------------------------------------------------------------------
// Reverse sync: DAG events → filesystem
// ---------------------------------------------------------------------------

/// Handle a reverse sync event: write or delete a file based on an engine event.
///
/// Returns `Ok(true)` if the event was handled, `Ok(false)` if it was not a
/// file-related event.
///
/// Before writing, checks whether the file already exists on disk with the
/// correct content (by hashing). This avoids redundant I/O when the event was
/// generated by this device's own forward sync.
pub fn handle_reverse_sync_event(
    event: &EngineEvent,
    folders: &HashMap<FolderId, SyncedFolder>,
    storage: &Storage,
    echo_suppressor: &EchoSuppressor,
) -> Result<bool> {
    match event {
        EngineEvent::FileSynced {
            blob_hash,
            folder_id,
            path,
        } => {
            if let Some(folder) = folders.get(folder_id) {
                reverse_sync_file(folder, path, *blob_hash, storage, echo_suppressor)?;
            }
            Ok(true)
        }
        EngineEvent::FileModified {
            folder_id,
            path,
            new_hash,
        } => {
            if let Some(folder) = folders.get(folder_id) {
                reverse_sync_file(folder, path, *new_hash, storage, echo_suppressor)?;
            }
            Ok(true)
        }
        EngineEvent::FileDeleted { folder_id, path } => {
            if let Some(folder) = folders.get(folder_id) {
                delete_file_from_disk(folder, path, echo_suppressor)?;
            }
            Ok(true)
        }
        _ => Ok(false),
    }
}

/// Write a blob to a local file, skipping if the file already has the correct content.
fn reverse_sync_file(
    folder: &SyncedFolder,
    relative_path: &str,
    blob_hash: BlobHash,
    storage: &Storage,
    echo_suppressor: &EchoSuppressor,
) -> Result<()> {
    let full_path = folder.local_path.join(relative_path);

    // Skip if the file already exists with the correct content.
    // This avoids redundant reads from encrypted blob storage and disk writes
    // when the event originated from this device's own forward sync.
    if full_path.is_file()
        && let Ok(existing_hash) = hash_file(&full_path)
        && existing_hash == blob_hash
    {
        debug!(
            path = %relative_path,
            "reverse sync: file already has correct content, skipping"
        );
        return Ok(());
    }

    if let Some(data) = storage.load_blob(blob_hash)? {
        write_file_to_disk(folder, relative_path, &data, echo_suppressor)?;
    } else {
        warn!(
            %blob_hash,
            path = relative_path,
            "blob not yet available for reverse sync"
        );
    }

    Ok(())
}

/// Write a blob to a local file path, creating parent directories as needed.
///
/// Marks the path for echo suppression so the filesystem watcher doesn't
/// re-sync the file we just wrote.
pub fn write_file_to_disk(
    folder: &SyncedFolder,
    relative_path: &str,
    data: &[u8],
    echo_suppressor: &EchoSuppressor,
) -> Result<()> {
    let full_path = folder.local_path.join(relative_path);

    // Create parent directories.
    if let Some(parent) = full_path.parent() {
        std::fs::create_dir_all(parent)
            .with_context(|| format!("create dirs for {}", full_path.display()))?;
    }

    // Mark for echo suppression BEFORE writing so the watcher sees it.
    echo_suppressor.mark_written(folder.folder_id, relative_path);

    std::fs::write(&full_path, data)
        .with_context(|| format!("write file {}", full_path.display()))?;

    debug!(path = %full_path.display(), size = data.len(), "wrote file to disk (reverse sync)");
    Ok(())
}

/// Delete a file from a local folder directory.
pub fn delete_file_from_disk(
    folder: &SyncedFolder,
    relative_path: &str,
    echo_suppressor: &EchoSuppressor,
) -> Result<()> {
    let full_path = folder.local_path.join(relative_path);

    if full_path.exists() {
        // Mark for echo suppression.
        echo_suppressor.mark_written(folder.folder_id, relative_path);

        std::fs::remove_file(&full_path)
            .with_context(|| format!("delete file {}", full_path.display()))?;

        debug!(path = %full_path.display(), "deleted file from disk (reverse sync)");

        // Clean up empty parent directories up to the folder root.
        let mut dir = full_path.parent();
        while let Some(parent) = dir {
            if parent == folder.local_path {
                break;
            }
            // Try to remove — fails silently if non-empty.
            if std::fs::remove_dir(parent).is_err() {
                break;
            }
            dir = parent.parent();
        }
    } else {
        debug!(
            path = %full_path.display(),
            "file already absent on disk (reverse sync delete)"
        );
    }

    Ok(())
}

// ---------------------------------------------------------------------------
// Forward sync: filesystem events → engine
// ---------------------------------------------------------------------------

/// Process a forward sync event (local filesystem change → engine operation).
///
/// Called from the main event loop when the watcher produces a debounced event.
pub fn handle_forward_sync_event(
    event: &FolderEvent,
    folders: &HashMap<FolderId, SyncedFolder>,
    engine: &mut MurmurEngine,
) -> Result<()> {
    let folder = match folders.get(&event.folder_id) {
        Some(f) => f,
        None => {
            warn!(
                folder_id = %hex::encode(event.folder_id.as_bytes()),
                "forward sync event for unknown folder"
            );
            return Ok(());
        }
    };

    // Read-only folders don't forward local changes.
    if folder.mode == SyncMode::ReadOnly {
        debug!(
            path = %event.relative_path,
            "skipping forward sync for read-only folder"
        );
        return Ok(());
    }

    let full_path = folder.local_path.join(&event.relative_path);

    match event.kind {
        FsEventKind::CreateOrModify => {
            if !full_path.is_file() {
                // Might be a directory creation or a file that was already deleted.
                return Ok(());
            }

            let state = engine.state();
            let key = (event.folder_id, event.relative_path.clone());
            let existing = state.files.get(&key).cloned();

            if let Some(existing_meta) = existing {
                // File already in DAG — read once, hash once, then pass to engine.
                let data = std::fs::read(&full_path)
                    .with_context(|| format!("read {}", full_path.display()))?;
                let current_hash = BlobHash::from_data(&data);

                if current_hash == existing_meta.blob_hash {
                    debug!(path = %event.relative_path, "file unchanged, skipping");
                    return Ok(());
                }

                info!(path = %event.relative_path, "forward sync: file modified");
                let size = data.len() as u64;
                let modified_at = file_modified_time(&full_path);
                let new_meta = FileMetadata {
                    blob_hash: current_hash,
                    folder_id: event.folder_id,
                    path: event.relative_path.clone(),
                    size,
                    mime_type: existing_meta.mime_type.clone(),
                    created_at: existing_meta.created_at,
                    modified_at,
                    device_origin: engine.device_id(),
                };
                engine.modify_file(event.folder_id, &event.relative_path, new_meta, data)?;
            } else {
                // New file → add.
                info!(path = %event.relative_path, "forward sync: new file");
                engine.add_file_streaming(event.folder_id, &event.relative_path, &full_path)?;
            }
        }
        FsEventKind::Remove => {
            let state = engine.state();
            let key = (event.folder_id, event.relative_path.clone());
            let in_dag = state.files.contains_key(&key);
            if in_dag {
                info!(path = %event.relative_path, "forward sync: file deleted");
                engine.delete_file(event.folder_id, &event.relative_path)?;
            }
        }
    }

    Ok(())
}

